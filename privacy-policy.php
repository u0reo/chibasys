        <p><strong>3-1-2．保存期間について</strong></p>
        <p>当サイトに残されたコメントは、コメントとそのメタデータを3年間保存します。</p>
        <p><strong>3-1-3．当サイトがコメントに対してできることについて</strong></p>
        <p>
          訪問者が当サイトにコメントを残したことがある場合は、当サイトの管理人は、当サイトが保存する訪問者についての個人情報
          （提供したすべての情報を含む）をエクスポートファイルとして訪問者が「受け取りたい」というリクエストに応えることが出来ます。
        </p>
        <p>
