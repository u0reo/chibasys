        <p><strong>3．個人情報の取得と利用目的</strong></p>
        <p>当サイトで取得する訪問者の個人情報と利用目的、保存期間等は下記の通りです。</p>
        <p><strong>3-1．コメントを残した際に個人情報を取得します</strong></p>
        <p>当サイトでは、訪問者が当サイトにコメントを残した際に、以下の個人情報を取得します。</p>
        <ul>
          <li>コメントフォームに入力された名前</li>
