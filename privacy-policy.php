          これには、管理や法律、セキュリティ目的のために保持する義務がある情報は含まれません。
        </p>
        <p><strong>3-2．Cookieによる個人情報の取得</strong></p>
        <p>当サイトは、訪問者のコンピュータにCookieを送信することがあります。</p>
        <p>Cookie（クッキー）とは、ウェブサイトを利用したときに、ブラウザとサーバーとの間で送受信した利用履歴や入力内容などを、
          訪問者のコンピュータにファイルとして保存しておく仕組みです。</p>
        <p><strong>3-2-1．利用目的について</strong></p>
        <p>訪問者の当サイト閲覧時の利便性を高めるためです。</p>
        <p>たとえば、次回同じページにアクセスするとCookieの情報を使って、ページの運営者は訪問者ごとに表示を変えることができます。</p>
        <p>たとえばあるサイトを利用していて、初回はログインパスワードを入力する画面が表示されたけど、
          2回目以降はログイン画面は表示されずにアクセスできた、という経験ありませんか？それはCookieによるものです。</p>
        <p>訪問者がブラウザの設定でCookieの送受信を許可している場合、ウェブサイトは、訪問者のブラウザからCookieキーを取得できます。</p>
        <p>なお、訪問者のブラウザはプライバシー保護のため、そのウェブサイトのサーバーが送受信したCookieのみを送信します。</p>
        <p><strong>3-2-2．保存期間について</strong></p>
        <p>当サイトに残されたコメントのCookieは保存されません。</p>
        <p><strong>3-2-3．第三者によるCookie情報の取得について</strong></p>
        <p>当サイトでは、グーグル株式会社やヤフー株式会社などをはじめとする第三者から配信される広告が掲載される場合があり、
          これに関連して当該第三者が訪問者のCookie情報等を取得して、利用している場合があります。</p>
        <p>当該第三者によって取得されたCookie情報等は、当該第三者のプライバシーポリシーに従って取り扱われます。</p>
        <p><strong>3-2-4．第三者へのCooke情報等の広告配信の利用停止について</strong></p>
        <p>訪問者は、当該第三者のウェブサイト内に設けられたオプトアウト（個人情報を第三者に提供することを停止すること）ページにアクセスして、
          当該第三者によるCookie情報等の広告配信への利用を停止することができます。</p>
        <p><strong>3-2-5．Cookie情報の送受信の許可・拒否について</strong></p>
        <p>
          訪問者は、Cookieの送受信に関する設定を「すべてのCookieを許可する」、「すべてのCookieを拒否する」、
          「Cookieを受信したらユーザーに通知する」などから選択できます。
          設定方法は、ブラウザにより異なります。Cookieに関する設定方法は、お使いのブラウザの「ヘルプ」メニューでご確認ください。
        </p>
        <p>すべてのCookieを拒否する設定を選択されますと、認証が必要なサービスを受けられなくなる等、インターネット上の各種サービスの利用上、制約を受ける場合がありますのでご注意ください。</p>
